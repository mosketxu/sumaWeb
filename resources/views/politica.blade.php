    <h1 class="display-5">Política de privacidad</h1>

    <h2 class="display-7">Introducción</h2>
    <p>El objetivo de esta política de privacidad es explicar, de acuerdo con lo establecido en el Reglamento 2016/679 General
        de Protección de Datos de la Unión Europea y en su normativa de desarrollo, tanto nacional como comunitaria, vigente
        en cada momento, la forma en la que Suma Apoyo Empresarial tratará los datos personales que pueda recabar.</p>

    <h2 class="display-7">¿Quién es el responsable del tratamiento de sus datos personales?</h2>
    <p>La responsabilidad del tratamiento recae en Suma Apoyo Empresarial.</p>

    <h2 class="display-7">¿Quién es la autoridad de control de protección de datos personales?</h2>
    <p>La autoridad de control es la Agencia Española de Protección de Datos, con sede en Madrid (28001), Calle Jorge Juan,
        nº 6, ante quien se pueden plantear consultas y/o reclamaciones relativas a esta materia, en caso de que usted considere
        que sus derechos y libertades de protección de datos no han sido debidamente atendidos por Suma Apoyo Empresarial.
        Para más información, puede consultar la página web siguiente: <a href="https://www.agpd.es/" target="_blank"><span style='color:#C9242E'>https://www.agpd.es</a>.</p>

    <h2 class="display-7">¿Qué datos personales son objeto de tratamiento?</h2>
    <p>Serán objeto de tratamiento todos los datos personales, recogidos por cualquier medio, incluyendo los documentos que
        los contengan, antes, durante y con posterioridad a la formalización de una solicitud, contrato o servicio solicitado
        a Suma Apoyo Empresarial por parte del cliente, que sean precisos para el estudio, emisión, desarrollo y ejecución
        de la relación contractual.</p>

    <p>En caso de que los datos personales sean aportados por persona distinta de su titular, recaerá en el aportante la obligación
        de trasladar previamente esta información al titular de los datos personales, así como de recabar su consentimiento
        cuando sea necesario para su tratamiento por parte de Suma Apoyo Empresarial.</p>

    <p>Asimismo, dichos datos personales podrán ser complementados, cumpliendo los requisitos previstos en la normativa sobre
        protección de datos personales, por otros obtenidos de fuentes de acceso público o por datos personales que el interesado
        haya hecho manifiestamente públicos.</p>

    <h2 class="display-7">¿Cuáles son las finalidades del tratamiento de los datos personales?</h2>
    <p>La finalidad principal del tratamiento de los datos personales es la administración y gestión general del despacho con
        la intención de prestar un buen servicio a sus clientes y poder mantener el contacto con los mismos u otros contactos
        profesionales. Asimismo, Suma Apoyo Empresarial, puede utilizar estos datos personales para enviar información comercial,
        jurídica y cualquier otra que Suma Apoyo Empresarial, S.L. considere de interés para sus clientes y/o terceros.</p>

    <h3>Decisiones automatizadas y perfiles:</h3>
    <p>Suma Apoyo Empresarial no realizará decisiones automatizadas, ni creaciones de perfiles con sus datos.</p>

    <h2 class="display-7">¿Durante cuánto tiempo conservaremos sus datos personales?</h2>
    <p>Los datos personales se conservarán durante todo el tiempo que esté vigente la relación con la Suma Apoyo Empresarial.
        Una vez finalizada dicha relación, se conservarán durante el tiempo necesario establecido por la normativa que resulte
        de aplicación en cada momento, quedando a disposición de los Juzgados y Tribunales, el Ministerio Fiscal, las Fuerzas
        y Cuerpos de Seguridad del Estado y/o las Administraciones Públicas competentes, en particular las autoridades de
        control de protección de datos personales competentes, y los Órganos supervisores correspondientes, para la atención
        de las posibles responsabilidades legales o contractuales derivadas del contrato o servicio en el que se ha basado
        el tratamiento y durante el plazo de prescripción de las mismas.</p>

    <p>Las solicitudes o propuestas que no lleguen a materializarse en un contrato o servicio, independientemente del motivo,
        serán conservadas durante el plazo necesario para garantizar la finalidad de lucha contra el fraude en la contratación
        y de prevenir el blanqueo de capitales y la financiación del terrorismo.</p>

    <h2 class="display-7">¿A qué destinatarios se comunicarán sus datos personales?</h2>
    <p>Los datos personales podrán ser comunicados a los diferentes colaboradores y prestadores de servicios de Suma Apoyo Empresarial,
        tales como, a título meramente enunciativo y sin carácter limitativo: distribuidores de seguros, coaseguradores,
        reaseguradores, peritos, abogados, auditores, consultores, profesionales y evaluadores de la salud, entidades financieras,
        entidades depositarias, entidades gestoras y otros proveedores y profesionales, quienes tratarán los datos personales
        como encargados del tratamiento en nombre y por cuenta de Suma Apoyo Empresarial, con la finalidad de garantizar
        los servicios prestados por dicho responsable en la ejecución del contrato o servicio, cumplir con las obligaciones
        derivadas de la normativa aplicable, en interés legítimo y/o conforme el consentimiento prestado en su caso.
    </p>

    <p>Le informamos además específicamente, de que las Entidades colaboradoras de Suma Apoyo Empresarial, comparten con diferente
        grado de integración, servicios comunes con el fin de aprovechar las sinergias existentes, optimizar recursos y ofrecer

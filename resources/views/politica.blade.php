            usted puede solicitar información sobre los datos personales que Suma Apoyo Empresarial tengasobre su persona y sobre los
            tratamientos de los que están siendo objeto, así como obtener una copia de los mismos en un formato estructurado,
            de uso común y fácil lectura.
        </li>

        <li>
            <h4>Derecho de rectificación.</h4>
            usted puede solicitar la rectificación de los datos personales que resulten inexactos, e igualmente tiene derecho a solicitar

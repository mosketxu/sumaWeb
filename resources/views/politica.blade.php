        </li>

        <li>
            <h4>Derecho de supresión.</h4>
            usted puede solicitar la supresión de sus datos personales cuando los mismos ya no sean necesarios para los fines para losquefueron
            recogidos por Suma Apoyo Empresarial, o cuando usted retire el consentimiento en que se basael tratamiento. No
            será aplicable dicha solicitud cuando el tratamiento sea necesario en base a lo señalado en el apartado “legitimación
            del tratamientode los datos personales” anterior.
        </li>

        <li>
            <h4>Derecho de oposición.</h4>

            el responsable se oponga a la supresión de datos por ser lícito el tratamiento, (iii) cuando el responsable ya
            no necesite los datos pero sean precisos para la formulación, el ejercicio o la defensa de reclamaciones o, (iv)
            cuando usted se haya opuesto al tratamiento, mientras el responsable verifica si los motivos legítimos del responsable
